    // Procura o numero do candidato na lista recebida
    private boolean verificaCandidato() {
        boolean bEncontra=false;
        if(jLabelNumSpace.getText().length() == 5){
            for(int i=0; i<cliente.getiTamanhoLista(); i++){
                if(Integer.parseInt(jLabelNumSpace.getText()) == cliente.getiCodigoVotacao(i)){
                    jLabelNomeSpace.setText(cliente.getsNomeCandidato(i));
                    jLabelPartSpace.setText(cliente.getsPartidoCandidato(i));
                    jLabelImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource(cliente.getsFilePathCandidato(i))));
                    
                    jLabelConfirma1.setVisible(true);
                    jLabelConfirma2.setVisible(true);
                    
                    bEncontra=true;
                }
            }
            if(bEncontra==false){
                iModoUI=2;
            }
        }
        return bEncontra;
    }
    
    // Faz o tratamento a partir de setVisible()
    private static void tratamentoDeTelas() {
        while(true){
            // Selecao de opcao
            if(iModoUI == 0){
                jPanelSelecao.setVisible(true);
                jPanelVota.setVisible(false);
                jPanelNaoEncontrado.setVisible(false);
                jPanelCarrega.setVisible(false);
                jPanelFinal.setVisible(false);
            }
            // Voto do cliente
            else if(iModoUI == 1){
                jPanelSelecao.setVisible(false);
                jPanelVota.setVisible(true);
                jPanelNaoEncontrado.setVisible(false);
                jPanelCarrega.setVisible(false);
                jPanelFinal.setVisible(false);
            }
            // Candidato nao encontrado
            else if(iModoUI == 2){
                jPanelSelecao.setVisible(false);
                jPanelVota.setVisible(false);
                jPanelNaoEncontrado.setVisible(true);
                jPanelCarrega.setVisible(false);
                jPanelFinal.setVisible(false);
            }
            // Carregar lista de candidatos
            else if(iModoUI == 3){
                jPanelSelecao.setVisible(false);
                jPanelVota.setVisible(false);
                jPanelNaoEncontrado.setVisible(false);
                jPanelCarrega.setVisible(true);
                jPanelFinal.setVisible(false);
            }
            // Finalizar votacoes
            else if(iModoUI == 4){
                jPanelSelecao.setVisible(false);
                jPanelVota.setVisible(false);
                jPanelNaoEncontrado.setVisible(false);
                jPanelCarrega.setVisible(false);
                jPanelFinal.setVisible(true);
            }
        }
    }
    
